package android.renderscript;


/**
 * Vector version of the basic long type.
 * Provides four long fields packed.
 * @apiSince 11
 */

@SuppressWarnings({"unchecked", "deprecation", "all"})
public class Long4 {

/** @apiSince 11 */

public Long4() { throw new RuntimeException("Stub!"); }

/** @apiSince 14 */

public Long4(long x, long y, long z, long w) { throw new RuntimeException("Stub!"); }

/** @apiSince 11 */

public long w;

/** @apiSince 11 */

public long x;

/** @apiSince 11 */

public long y;

/** @apiSince 11 */

public long z;
}

