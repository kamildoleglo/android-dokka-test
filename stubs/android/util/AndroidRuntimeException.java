package android.util;


/**
 * Base class for all unchecked exceptions thrown by the Android frameworks.
 * @apiSince 1
 */

@SuppressWarnings({"unchecked", "deprecation", "all"})
public class AndroidRuntimeException extends java.lang.RuntimeException {

/** @apiSince 1 */

public AndroidRuntimeException() { throw new RuntimeException("Stub!"); }

/** @apiSince 1 */

public AndroidRuntimeException(java.lang.String name) { throw new RuntimeException("Stub!"); }

/** @apiSince 11 */

public AndroidRuntimeException(java.lang.String name, java.lang.Throwable cause) { throw new RuntimeException("Stub!"); }

/** @apiSince 1 */

public AndroidRuntimeException(java.lang.Exception cause) { throw new RuntimeException("Stub!"); }
}

